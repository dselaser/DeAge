void StartDefaultTask(void const * argument)
{
  /* USER CODE BEGIN 5 */

	uint8_t Mag1 ;

	HAL_Delay(500);
	DF_Busy();

	// LED 12개를 차례로 점등하면서 동작할 준비가 되어있다를 표시함
    for (int i = 0; i < 12; i++) //Turn On LED step by step
    {
    	 HAL_GPIO_WritePin(cs_pins[i].port, cs_pins[i].pin, 1);
    	 HAL_Delay(200);
    }
   // HAL_Delay(500);

	// 전기 넣으면 시작메시지를 내보냄

	MP3_Message(5);
	HAL_Delay(1000);

    LED_Show(0);
    LED_Show(1);
    LED_Show(0);
    LED_Show(1);
    LED_Show(0);
    LED_Show(1);
    LED_Show(0);

  for(;;)//------------- MAIN Routine -------------------------
  {
	MP3_Message(6);

	HAL_Delay(2000);


	while (1)
	    {
		    MP3_Message(7);
	    	HAL_Delay(2000);

	    	// 핸들이 내려왔는지 검출, PCB 위에 있는 자석을 Hall sensor 사용해서 검출함
	    	// GPIOC의 PIN_13에서 현재 상태 읽기
	    	Mag1 = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);

	        // Mag1 값이 0이면 루프를 빠져나옴
	        if (Mag1 == GPIO_PIN_RESET) // GPIO_PIN_RESET은 0 상태를 의미합니다.
	        {
	            break;
	        }
	    }

	HAL_Delay(1000);
	EEPROM_Erase_And_Verify(&hspi2);  // U2부터 하나씩 0으로 쓰고 verify, 결과를 LED로 표시

	HAL_Delay(500);


	MP3_Message(1);
	HAL_Delay(1000);


	while (1)
	    {
			MP3_Message(4);
	    	HAL_Delay(2000);

	    	 // GPIOC의 PIN_13에서 현재 상태 읽기
	    	Mag1 = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);

	    		        // Mag1 값이 0이면 루프를 빠져나옴
	        if (Mag1 != GPIO_PIN_RESET) // GPIO_PIN_RESET은 0 상태를 의미합니다.
	        {
	            break;
	        }
	    }

  }
  /* USER CODE END 5 */
}

